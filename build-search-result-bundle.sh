#!/usr/bin/env bash
#
# Builds search-result-bundle.js = ordotype-index + search-result-filter, so the
# /search-result page loads ONE self-contained file. With both halves in a single
# request there is no cross-script load race and the filter can never run without
# the index — fixes Sentry ORDOTYPE-FRONTEND-19J at the root.
#
# IMPORTANT: keep the two filenames below in sync with the loader (Webflow custom
# code). The loader serves $INDEX on every non-search page and search-result-bundle.js
# on /search-result. When you bump either source version, update the filename here.
#
set -euo pipefail
cd "$(dirname "$0")"

INDEX="ordotype-index-2026-04-07.js"
FILTER="search-result-filter-2026-01-22.js"
OUT="search-result-bundle.js"

for f in "$INDEX" "$FILTER"; do
  [ -f "$f" ] || { echo "ERROR: source file '$f' not found" >&2; exit 1; }
done

{
  echo "// AUTO-GENERATED by build-search-result-bundle.sh — DO NOT EDIT BY HAND."
  echo "// Sources: $INDEX + $FILTER"
  echo "// Loaded only on /search-result so the filter can never run without the index."
  echo ""
  echo "// ===== $INDEX ====="
  cat "$INDEX"
  echo ""
  echo "// ===== $FILTER ====="
  cat "$FILTER"
} > "$OUT"

echo "Built $OUT ($(wc -l < "$OUT" | tr -d ' ') lines, $(wc -c < "$OUT" | tr -d ' ') bytes)"
